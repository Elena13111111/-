#часы * на 60 минут * на 60 секунд. берем 1 час.
print(f'В 1 часе= {1 * 60 *60} секунд')
seconds_per_hours = 1 * 60 *60
#  секунды в сутках
seconds_per_day = seconds_per_hours *24
print(f'В сутках = {seconds_per_day} секунд')
# разделение 2-ух переменны (/)
print(seconds_per_day / seconds_per_hours)
# разделение 2-ух переменны (//)
print(seconds_per_day // seconds_per_hours)
